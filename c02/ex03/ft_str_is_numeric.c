void	ft_str_is_numeric(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (48 <= str [i] <= 57)
			i++;
		else
			return (0);
	}
	if (str[i] == '\0')
	{
		return (1);
	}
}
