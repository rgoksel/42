#include <unistd.h>

void	ft_print_comb2(void)
{
	char	a[5];
	int		j;
	int		i;

	j = 1;
	i = 0;
	while (j <= 99)
	{
		a[0] = '0' + (i / 10);
		a[1] = '0' + (i % 10);
		a[2] = ' ';
		a[3] = '0' + (j / 10);
		a[4] = '0' + (j % 10);
		write (1, a, 5);
		if (!(j == 99 && i == 98))
			write(1, ", ", 2);
		if (++j > 99)
		{
			i++;
			j = i +1;
		}
	}	
}
